void usercontrol(void) {
  int threshold = 15;
  while (1) {
    
    int x = Controller1.Axis3.position(percent);
    int y = -Controller1.Axis1.position(percent);
    
    if (x >= -threshold && x <= threshold) {
      x = 0;
    } else if (y >= -threshold && y <= threshold) {
      y = 0;
    }

    int leftSpeed = y + x;
    int rightSpeed = y - x;

    LeftWheels.setVelocity(leftSpeed * 2, rpm);
    RightWheels.setVelocity(rightSpeed * 2, rpm);

    LeftWheels.spin(forward);
    RightWheels.spin(forward);

    wait(20, msec); // Sleep the task for a short amount of time to
                    // prevent wasted resources.
  }
}

//
// Main will set up the competition functions and callbacks.
//
int main() {
  // Set up callbacks for autonomous and driver control periods.
  Competition.autonomous(autonomous);
  Competition.drivercontrol(usercontrol);

  // Run the pre-autonomous function.
  pre_auton();

  // Prevent main from exiting with an infinite loop.
  while (true) {
    wait(100, msec);
  }
}
